package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T5> the type of the fifth element of the tuple
 */
public interface ISetMutableValue5<T5> {

    /**
     * Sets the fifth element of the tuple.
     *
     * @param _5 the element to set
     */
    void _5(T5 _5);

    /**
     * Sets the fifth element of the tuple.
     *
     * @param fifth the element to set
     */
    void fifth(T5 fifth);

}
