package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T8> the type of the eighth element of the tuple
 */
public interface ISetMutableValue8<T8> {

    /**
     * Sets the eighth element of the tuple.
     *
     * @param _8 the element to set
     */
    void _8(T8 _8);

    /**
     * Sets the eighth element of the tuple.
     *
     * @param eighth the element to set
     */
    void eighth(T8 eighth);

}
