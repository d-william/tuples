package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T4> the type of the fourth element of the tuple
 */
public interface ISetMutableValue4<T4> {

    /**
     * Sets the fourth element of the tuple.
     *
     * @param _4 the element to set
     */
    void _4(T4 _4);

    /**
     * Sets the fourth element of the tuple.
     *
     * @param fourth the element to set
     */
    void fourth(T4 fourth);

}
