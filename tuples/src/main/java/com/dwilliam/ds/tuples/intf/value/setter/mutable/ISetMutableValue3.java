package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T3> the type of the third element of the tuple
 */
public interface ISetMutableValue3<T3> {

    /**
     * Sets the third element of the tuple.
     *
     * @param _3 the element to set
     */
    void _3(T3 _3);

    /**
     * Sets the third element of the tuple.
     *
     * @param third the element to set
     */
    void third(T3 third);

}
