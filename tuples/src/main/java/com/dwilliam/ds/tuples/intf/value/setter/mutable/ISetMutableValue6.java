package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T6> the type of the sixth element of the tuple
 */
public interface ISetMutableValue6<T6> {

    /**
     * Sets the sixth element of the tuple.
     *
     * @param _6 the element to set
     */
    void _6(T6 _6);

    /**
     * Sets the sixth element of the tuple.
     *
     * @param sixth the element to set
     */
    void sixth(T6 sixth);

}
