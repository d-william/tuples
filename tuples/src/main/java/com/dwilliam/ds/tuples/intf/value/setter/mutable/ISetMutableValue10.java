package com.dwilliam.ds.tuples.intf.value.setter.mutable;

/**
 * Marker interface for mutable tuples.
 *
 * @param <T10> the type of the tenth element of the tuple
 */
public interface ISetMutableValue10<T10> {

    /**
     * Sets the tenth element of the tuple.
     *
     * @param _10 the element to set
     */
    void _10(T10 _10);

    /**
     * Sets the tenth element of the tuple.
     *
     * @param tenth the element to set
     */
    void tenth(T10 tenth);

}
